package Task1;

public class OutputTask {
    public static void main(String[] arg){
        //Output
        System.out.println("Good Morning");
        System.out.println("Hii");

        //Right Half
        System.out.println("*");
        System.out.println("**");
        System.out.println("***");
        System.out.println("****");
        System.out.println("*****\n");

        //Reverse Right Half
        System.out.println("*****");
        System.out.println("****");
        System.out.println("***");
        System.out.println("**");
        System.out.println("*");

        //Left Half
        System.out.println("    *");
        System.out.println("   **");
        System.out.println("  ***");
        System.out.println(" ****");
        System.out.println("*****");
    }
}
